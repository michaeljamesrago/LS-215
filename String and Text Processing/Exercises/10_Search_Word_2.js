"use strict";

function searchWord(word, text) {
  let indexes = [];

  let exp = '\\b' + word + '\\b';
  let regExp = new RegExp(exp, 'gi');

  let match = regExp.exec(text);
  while (match) {
    indexes.push(match.index);
    match = regExp.exec(text);
  }

  let startIndex = 0;
  let textParts = indexes.map( function(index) {
    let endIndex = index + word.length;
    let textPart = text.slice(startIndex, endIndex);
    startIndex = endIndex;
    return textPart;
  });

  textParts.push(text.slice(startIndex));

  let highlightedTextParts = textParts.map( function(textPart) {
    return textPart.replace(regExp, `**${word.toUpperCase()}**`);
  })
  return highlightedTextParts.join('');
}

